public class Emp {
    private String name;
    private String emp_id;

    public void setName (String name)
    {
        this.name = name;
    }
    public String getName()
    {
        return name;
    }

    public void setEmp_id(String emp_id) {
        this.emp_id = emp_id;
    }
    public String getEmp_id() {
        return emp_id;
    }
}